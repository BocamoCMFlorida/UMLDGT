public class Propietario {
    private String DNI;
    private String Nombre;
    private String Apellidos;
    private int Puntos;

    public Propietario(String DNI, String nombre, String apellidos, int puntos) {
        this.DNI = DNI;
        Nombre = nombre;
        Apellidos = apellidos;
        Puntos = puntos;
    }
}
